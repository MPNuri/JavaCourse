package Day40.Car;

public abstract class Car {

    private String make;
    private String model;
    private int year;

    public Car(String make, String model, int name) {
        this.make=make;
        this.model=model;
        this.year=year;
    }

    public int getYear() {
        return year;
    }

    public String getModel() {
        return model;
    }

    public String getMake() {
        return make;
    }
































































































}
