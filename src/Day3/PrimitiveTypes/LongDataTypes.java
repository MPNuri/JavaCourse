package Day3.PrimitiveTypes;

public class LongDataTypes {
    public static void main(String[] args) {

        long worldPopulation = 7_000_000_000l;
        // to let java know that data type is not int but a long type L or l
        // this way you wont have a problem numbers over 2_000_000_000


        System.out.println(worldPopulation);

        Long myFirstLong;
        myFirstLong = 58_965_821_432L;
        System.out.println(myFirstLong);

        Long mySecondLongNegative = -86_424_322_098L;
        System.out.println(mySecondLongNegative);







    }
}