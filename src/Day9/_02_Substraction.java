package Day9;

public class _02_Substraction {
    public static void main(String[] args) {

        //classic subtraction
        System.out.println(55 + 33);

        //variable method
        int a = 95;
        int b = 67;
        System.out.println(a - b);

        // or both at the same time
        int result = a-b;
        System.out.println(result);

        //variable and value
        System.out.println(7 - b);

        // this will give you a negative value (-22)
        System.out.println(-result);

        //Math will not haappen here because of the logic
        System.out.println("result: " + a + b);
        //answer= result: 9567 because we are not adding we are putting these 2 variables one by one










    }
}
