package Day14;

public class _05_Task3ForLoop {
    public static void main(String[] args) {


        // use i instead of counter. this is how for loops are used

        for (int i = 0; i < 21; i++ ) {
            System.out.print(i + " ");
        }

        for (int i = 20; i >= 0; i--) {
            System.out.print(i + " ");
        }







    }
}
