package Day20;

public class Task1_ {
    public static void main(String[] args) {

       char[] chars = {'m', '1' ,'6','8' ,'9' };
       printAllChars(chars);

    }

    public static void printAllChars(char[] chars) {
        for (int i = 0; i < chars.length; i++) {
            System.out.println(chars[i]);
        }
    }


}
